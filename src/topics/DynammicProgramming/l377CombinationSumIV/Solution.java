package topics.DynammicProgramming.l377CombinationSumIV;

public class Solution {
    // what's the difference between this problem and leet 518 is that
    // the order doesn't matter in this question
    public int combinationSum4(int[] nums, int target) {
        int[] comb = new int[target + 1];
        comb[0] = 1;
        for (int i = 1; i < comb.length; i++) {
            for (int j = 0; j < nums.length; j++) {
                if (i - nums[j] >= 0) {
                    comb[i] += comb[i - nums[j]];
                }
            }
        }
        return comb[target];
    }
}
